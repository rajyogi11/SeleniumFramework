package DataCasting;

public class Implicit {
	
	public static void main(String[] args) {
		
		byte a = 78;
		System.out.println(a);  // Memory size is 1 byte
		
		int  b=a;     
		System.out.println(b);  // memory size is 4 byte
		
		long c = b ;
		System.out.println(c);  // memory size is 8 byte
		
	
}
}

 