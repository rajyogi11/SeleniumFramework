package Polymorphism;

public class Encapsulation {
	
	
	private int a =55;
	private int b =50;
	
	Encapsulation () {
		a=34; 
		b=65;
	}
	Encapsulation (int c, int d ) {
		a=90;
		b=50;
				
	}
	
	Encapsulation (int f, int g, int h){
		a=10;
		b=10;
	}
	public void Test () {
		int c = a+b;
		System.out.println(c);
	}
	
	}
		
	
	
	


