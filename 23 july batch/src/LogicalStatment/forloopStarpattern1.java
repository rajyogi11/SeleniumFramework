package LogicalStatment;

public class forloopStarpattern1 {
	
	public static void main(String[] args) {
		
		for (int i = 1; i<=5; i++) { // outer loop 5 * row
			
			
			for (int j =1; j<=i; j++) { // inner loop 3 * column
				
				System.out.print("*"); // increment
				
			}
			System.out.println();
				
		}
			
			
		}

	}
	


