package String;

public class UpperCase {
	
	public static void main(String[] args) {
		
		String a = "sachin";
		String b = "Sachin Devde";
		String c = "RAJYOGI";
		
		System.out.println(b.toUpperCase()); // upper case convert all string in to capital letter
		
		System.out.println(a);
		
		System.out.println(c.toLowerCase());// to lower case all string into lower case
	}

}
