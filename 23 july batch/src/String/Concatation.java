package String;

public class Concatation {
	
	public static void main(String[] args) {
		 
		String a = "Rajyogi";
		String b = "Gosavi";
		String c = "Sachin";
		
		System.out.println(a.concat(b));  // addition or concatation of 2 string
		
		System.out.println(c+" Tendulkar");
	}

}
