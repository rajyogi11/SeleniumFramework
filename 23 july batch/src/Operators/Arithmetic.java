package Operators;

public class Arithmetic {

	public static void main(String[] args) {
	
	byte a = 20, b = 10;
	
	//Addition
	
	System.out.println("Value of a + b =  "+ (a+b));
	
	
    // Multiplication
	System.out.print("Value of a * b =  "+ (a*b));
	
	// Substraction
	
	
	System.out.println("Value of a - b = "+ (a-b));
	
	// Divison
	
    System.out.println("Value of a / b = "+ (a/b));
	
	// Module
	
	System.out.println("Value of a % b = "+ (a%b));
	
	
	
	}	
}
