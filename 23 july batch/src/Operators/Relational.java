package Operators;

public class Relational {

  public static void main (String[] args){
	
	int a = 40, b = 20;
	
	
	// Value of a and b
	System.out.println("a value is "+a  +  "b value is "+ b);
	
	
	// Greator than operator
	System.out.println(b>a);
	
	//Less than operator
	System.out.println(b<a);
	
	//==operator
	System.out.println(a==b);
	
	//!- operator
	System.out.println(a!=b);
	
	
	
	}
}
